class DayNightMode extends Component {

  toggle = async () => {
    const { set, nightMode } = this.props;
    const data = { nightMode: !nightMode };
    localStorage.setItem('conf', JSON.stringify(data));
    set(data);
  }

  render() {
    const { nightMode } = this.props
    return (
      <div className="day-night-mode" style={{ lineHeight: '60px', float: 'right', marginTop: '4px' }} onClick={() => this.toggle()}>
        {nightMode ? <DayIcon /> : <NightIcon />}
      </div>
    )
  }
}

const mapStateToProps = (state) => {
  return {
    nightMode: state.conf.nightMode,
  }
};

export default connect(mapStateToProps, { set })(DayNightMode);
